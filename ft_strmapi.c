#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))

{
	char			*str;
	unsigned int	i;

	str = malloc(ft_strlen((char *)s) + 1);
	if (!str)
		return (NULL);
	i = -1;
	while (s[++i])
		str[i] = (*f)(i, s[i]);
	str[i] = '\0';
	return (str);
}
